    private void btn_ver_carros_reservadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ver_carros_reservadosActionPerformed
        // TODO add your handling code here:
        
        boolean tem_daddos_reserva=true;
        int index=-1;
        ArrayList <String> carro_tempos=new ArrayList<String> ();
         Reserva carro = new Reserva();
        
          for(int i = 0; i < carros_rv.size(); i ++){
            if(carros_rv.get(i).getTempo_carro().equals(tempo_carro_input.getText())){  
                tem_daddos_reserva=false;
                index=i;
                carro_tempos.add("\n"+carros_rv.get(i).getNome_carro());
            }   
        }
          
           if(tem_daddos_reserva||index==-1){
              
                JOptionPane.showMessageDialog(null, "Tempo de Reserva nÃ£o encontrado = "+tempo_carro_input.getText());
                 return;
            }
           
           if(!tem_daddos_reserva){
                JOptionPane.showMessageDialog(null, "Tempo de Reserva \n"+Arrays.toString( carro_tempos.toArray()));
                 return;
            }
        
    }//GEN-LAST:event_btn_ver_carros_reservadosActionPerformed

